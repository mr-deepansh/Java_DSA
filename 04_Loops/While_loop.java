/**
 * While_loop
 */
public class While_loop {

  public static void main(String[] args) {
    int i = 0;
    while (i < 10) {
      System.out.println("Hello Programmer ! How are you Today");
      i++;
    }

    // while loop second example
    int counter = 1;
    while (counter <= 10) {
      System.out.println("Counter is: " + counter);
      counter++;

    }
  }
}